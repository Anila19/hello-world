package Strings.Interviews;

public class LengthOfString {
public static void main(String[] args){
	String name = "mouse";
int result =	length(name);
System.out.println(result);
}
public static int length(String names){
	int i=0;
	try{
		for( i =0;;i++){
			
		names.charAt(i);
		}
	}catch(Exception e){
	System.out.println(e);
	}
	
	return i;
}
}
